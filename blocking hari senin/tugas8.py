# Mulai
umur = int(input("Masukkan umur: ")) # input umur
if umur < 10: # umur < 10
    print("Anak-anak") # Anak-anak
elif umur < 18: # umur < 18
    print("Remaja") # Remaja
elif umur < 35: # umur < 35
    print("Dewasa") # Dewasa
elif umur < 65: # umur < 65
    print("Paruhbaya") # Paruhbaya
else:
    print("Tua") # Tua
